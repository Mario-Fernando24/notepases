class NotePaseTypography {
  NotePaseTypography._();

  //fonts
// Fuente decorativa y expresiva.
// Ideal para títulos grandes, branding, encabezados o pantallas de bienvenida.
// NO recomendada para textos largos por legibilidad.
static const String familyCorben = 'Corben';

// Fuente moderna y muy legible, diseñada para interfaces.
// Excelente para apps profesionales, dashboards y textos UI.
// Muy usada como alternativa moderna a Roboto.
static const String familyInter = 'Inter';

// Fuente limpia y neutral, optimizada para lectura en pantallas.
// Ideal para textos largos, formularios, descripciones y contenido general.
// Muy común en apps funcionales y de productividad.
static const String familyOpenSans = 'OpenSans';

// Fuente estándar del sistema Android.
// Perfecta para consistencia nativa, buen rendimiento y compatibilidad.
// Ideal para apps que buscan look "Material" clásico.
static const String familyRoboto = 'Roboto';

